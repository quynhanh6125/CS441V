# Hàm tính thị phần top N + Others
def get_pie_data(year, top_n=10):
    s = df[['Country', year]].dropna().sort_values(year, ascending=False)
    top = s.head(top_n).copy()
    other_val = s.iloc[top_n:][year].sum()
    top.loc[len(top)] = ['Others / Khác', other_val]
    top = top.set_index('Country')
    top['GDP_T'] = top[year] / 1e6
    return top

