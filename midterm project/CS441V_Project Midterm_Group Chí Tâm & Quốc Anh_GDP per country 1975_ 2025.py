"""Hệ số Gini được tính bằng công thức (dạng data đã đc sắp xếp):
$$
G = \frac{2\sum_{i=1}^{n} i x_i}{n\sum_{i=1}^{n} x_i} - \frac{n+1}{n}
$$
"""

# Tính hệ số Gini cho từng năm từ data
def gini_coefficient(arr):
    """Tính Gini coefficient từ mảng giá trị GDP. Gini = 0: bình đẳng, Gini = 1: bất bình đẳng tuyệt đối."""
    arr = arr[~np.isnan(arr)]
    arr = np.sort(arr)
    n = len(arr)
    if n == 0: return np.nan
    index = np.arange(1, n + 1)
    return (2 * np.sum(index * arr) / (n * np.sum(arr))) - (n + 1) / n

# Áp dụng cho tất cả các năm
gini_series = {}
for y in years:
    gini_series[y] = gini_coefficient(df[y].values)

# Tạo DataFrame để plot
